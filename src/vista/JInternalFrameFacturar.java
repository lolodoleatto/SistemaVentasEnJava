    private void jTableProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProductosMouseClicked
        int filaPoint = jTableProductos.rowAtPoint(evt.getPoint());
        int columnaPoint = 0;

        if (filaPoint > -1) {
            idArrayList = (int) modeloDatosProductos.getValueAt(filaPoint, columnaPoint);
        }

        int opc = JOptionPane.showConfirmDialog(null, "¿Eliminar Producto?");
        //opciones del confirm dialog: si = 0, no = 1, cancel = 2, close = -1
        switch (opc) {
            case 0: // presiono si
                listaProductos.remove(idArrayList - 1);
                this.calcularTotalPagar();
                this.listaTablaProductos();
                break;
            case 1:
                break;
            default:
                break;
        }
    }//GEN-LAST:event_jTableProductosMouseClicked

    private void btnRegistrarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarVentaActionPerformed
        DetalleVenta detalleVenta = new DetalleVenta();
        Venta venta = new Venta();
        CtrlVenta ctrlVenta = new CtrlVenta();

        String fechaActual = "";
        Date date = new Date();
        fechaActual = new SimpleDateFormat("yyyy/MM/dd").format(date);

        if (!cmbCliente.getSelectedItem().equals("Seleccione cliente:")) {
            if (!listaProductos.isEmpty()) {

                this.obtenerIdCliente();
                //registrar venta
                venta.setIdVenta(0);
                venta.setIdCliente(idCliente);
                venta.setValorPagar(Double.parseDouble(txtTotal.getText()));
                venta.setFechaVenta(fechaActual);
                venta.setEstado(1);

                if (ctrlVenta.guardar(venta)) {
                    JOptionPane.showMessageDialog(null, "Venta guardada correctamente");
                    //generamos la factura de venta
                    VentaPDF pdf = new VentaPDF();
                    pdf.datosCliente(idCliente);
                    pdf.generarFacturaPDF();

                    for (DetalleVenta dv : listaProductos) {
                        detalleVenta.setIdDetalleVenta(0);
                        detalleVenta.setIdVenta(0);
                        detalleVenta.setIdProducto(dv.getIdProducto());
                        detalleVenta.setCantidad(dv.getCantidad());
                        detalleVenta.setPrecioUnitario(dv.getPrecioUnitario());
                        detalleVenta.setSubtotal(dv.getSubtotal());
                        detalleVenta.setDescuento(dv.getDescuento());
                        detalleVenta.setIva(dv.getIva());
                        detalleVenta.setTotalPagar(dv.getTotalPagar());
                        detalleVenta.setEstado(1);

                        if (ctrlVenta.guardarDetalle(detalleVenta)) {
                            System.out.println("Detalle de venta guardado correctamente");
                            auxIdDetalle = 1;
                            this.cargarComboCliente();
                            this.restarStockProductos(dv.getIdProducto(), dv.getCantidad());
                            txtSubtotal.setText("0.00");
                            txtIVA.setText("0.00");
                            txtDescuento.setText("0.00");
                            txtTotal.setText("0.00");
                        } else {
                            System.out.println("Error al guardar detalle de venta");
                        }

                    }
                    listaProductos.clear();
                    listaTablaProductos();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al guardar venta");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe agregar al menos un producto");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un cliente");
        }
    }//GEN-LAST:event_btnRegistrarVentaActionPerformed



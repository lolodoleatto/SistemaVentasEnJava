    private void btnAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnadirActionPerformed
        String combo = this.cmbProducto.getSelectedItem().toString();

        //validamos que seleccionen un prod
        if (combo.equalsIgnoreCase("Seleccione producto:")) {
            JOptionPane.showMessageDialog(null, "SELECCIONE UN PRODUCTO");
        } else {

            //validamos que ingrese una cantidad
            if (!txtCantidad.getText().isEmpty()) {

                //validamos que ingrese unicamente valores numericos
                if (esNumero(txtCantidad.getText().trim()) && Integer.parseInt(txtCantidad.getText()) > 0) {
                    cantidad = Integer.parseInt(txtCantidad.getText());
                    this.datosDelProducto();
                    //validar que la cantidad del producto no sea mayor a la de la BDD
                    if (cantidad <= cantidadProductosBDD) {

                        // calculamos y redondeamos a dos decimales
                        subtotal = precioUnitario * cantidad;
                        subtotal = (double) Math.round(subtotal * 100) / 100;

                        totalPagar = subtotal + iva + descuento;
                        iva = (double) Math.round(iva * 100) / 100;
                        descuento = (double) Math.round(descuento * 100) / 100;
                        totalPagar = (double) Math.round(totalPagar * 100) / 100;

                        // creamos el nuevo producto
                        producto = new DetalleVenta(auxIdDetalle, 1, idProducto, descripcion, cantidad, precioUnitario, subtotal, descuento, iva, totalPagar, "fecha", 1);
                        // añadimos a la lista

                        listaProductos.add(producto);
                        JOptionPane.showMessageDialog(null, "PRODUCTO AÑADIDO");
                        auxIdDetalle++;
                        txtCantidad.setText("");
                        this.cargarComboProductos();
                        this.calcularTotalPagar();

                    } else {
                        JOptionPane.showMessageDialog(null, "HA SOBREPASADO EL STOCK. El stock actual de " + descripcion + " es de: " + cantidadProductosBDD);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "SOLO SE ADMITEN DATOS NUMERICOS MAYORES A 0 EN CANTIDAD");
                }
            } else {
                JOptionPane.showMessageDialog(null, "INGRESA UNA CANTIDAD");
            }
        }
        this.listaTablaProductos();
    }//GEN-LAST:event_btnAnadirActionPerformed

    private void btnCambioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambioActionPerformed
        if (!txtEfectivo.getText().isEmpty()) {
            if (esDouble(txtEfectivo.getText())) {
                double eft = Double.parseDouble(txtEfectivo.getText().trim());
                double totalPag = Double.parseDouble(txtTotal.getText().trim());

                if (eft >= totalPag) {
                    double cambio = eft - totalPag;
                    cambio = (double) Math.round(cambio * 100) / 100;
                    txtCambio.setText(String.valueOf(cambio));
                } else {
                    JOptionPane.showMessageDialog(null, "EL DINERO EN EFECTIVO NO ES SUFICIENTE");
                }
            } else {
                JOptionPane.showMessageDialog(null, "REVISE LOS VALORES INGRESADOS EN EFECTIVO");
            }
        } else {
            JOptionPane.showMessageDialog(null, "INGRESA EL DINERO EN EFECTIVO");
        }
    }//GEN-LAST:event_btnCambioActionPerformed


    //METODO PARA RESTAR EL STOCK EN LA BD
    private void restarStockProductos(int idProducto, int cantidad) {
        String sqlSelect = "SELECT cantidad FROM tb_producto WHERE idProducto = ?";
        String sqlUpdate = "UPDATE tb_producto SET cantidad = ? WHERE idProducto = ?";

        try (Connection cn = Conexion.conectar(); PreparedStatement selectStmt = cn.prepareStatement(sqlSelect); PreparedStatement updateStmt = cn.prepareStatement(sqlUpdate)) {

            // Obtener el stock actual
            selectStmt.setInt(1, idProducto);
            ResultSet rs = selectStmt.executeQuery();

            if (rs.next()) {
                int stockActual = rs.getInt("cantidad");
                int nuevoStock = stockActual - cantidad;

                if (nuevoStock < 0) {
                    System.out.println("Error: Stock insuficiente para el producto ID " + idProducto);
                    return;
                }

                // Actualizar stock
                updateStmt.setInt(1, nuevoStock);
                updateStmt.setInt(2, idProducto);

                if (updateStmt.executeUpdate() > 0) {
                    System.out.println("Stock actualizado correctamente para producto ID " + idProducto);
                }
            } else {
                System.out.println("Producto con ID " + idProducto + " no encontrado.");
            }

        } catch (SQLException e) {
            System.out.println("Error al restar stock: " + e.getMessage());
        }
    }


    private void cargarTablaVentas() {
        DefaultTableModel model = new DefaultTableModel();
        String sql = "SELECT v.idVenta AS id, "
                + "CONCAT(c.nombre, ' ', c.apellido) AS cliente, "
                + "v.valorPagar AS total, "
                + "v.fechaVenta AS fecha, "
                + "v.estado "
                + "FROM tb_venta AS v "
                + "JOIN tb_cliente AS c ON v.idCliente = c.idCliente;";

        model.addColumn("Nro");
        model.addColumn("Cliente");
        model.addColumn("Total Pagar");
        model.addColumn("Fecha Venta");
        model.addColumn("Estado");

        try (Connection con = Conexion.conectar(); Statement st = con.createStatement(); ResultSet rs = st.executeQuery(sql)) {

            while (rs.next()) {
                Object fila[] = new Object[5];
                for (int i = 0; i < 5; i++) {
                    if (i == 4) {
                        String estado = String.valueOf(rs.getObject(i + 1));
                        fila[i] = estado.equalsIgnoreCase("1") ? "Activo" : "Inactivo";
                    } else {
                        fila[i] = rs.getObject(i + 1);
                    }
                }
                model.addRow(fila);
            }

        } catch (SQLException e) {
            System.out.println("Error al cargar la tabla: " + e.getMessage());
        }
        JInternalFrameGestionarVenta.jTableVenta = new JTable(model);
        JInternalFrameGestionarVenta.jScrollCategoria.setViewportView(JInternalFrameGestionarVenta.jTableVenta);

        jTableVenta.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int filaPoint = JInternalFrameGestionarVenta.jTableVenta.rowAtPoint(e.getPoint());
                int columnaPoint = 0;

                if (filaPoint > -1) {
                    idVenta = Integer.parseInt(model.getValueAt(filaPoint, columnaPoint).toString());
                    EnviarDatosVenta(idVenta);
                }
            }
        });
    }

    private void EnviarDatosVenta(int idVenta) {
        String sql = "SELECT v.idVenta, "
                + "v.idCliente, "
                + "CONCAT(c.nombre, ' ', c.apellido) AS cliente, "
                + "v.valorPagar AS total, "
                + "v.fechaVenta AS fecha, "
                + "v.estado "
                + "FROM tb_venta AS v "
                + "JOIN tb_cliente AS c ON v.idCliente = c.idCliente "
                + "WHERE v.idVenta = ?";

        try (Connection con = Conexion.conectar(); PreparedStatement pst = con.prepareStatement(sql)) {

            pst.setInt(1, idVenta);

            try (ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    cmbCliente.setSelectedItem(rs.getString("cliente"));
                    txtTotalPagar.setText(rs.getString("total"));
                    txtFecha.setText(rs.getString("fecha"));
                    int estado = rs.getInt("estado");
                    cmbEstado.setSelectedItem(estado == 1 ? "Activo" : "Inactivo");
                }
            }

        } catch (SQLException e) {
            System.out.println("Error al seleccionar la venta: " + e.getMessage());
        }
    }

    private void limpiar() {
        txtTotalPagar.setText("");
        txtFecha.setText("");
        cmbCliente.setSelectedItem("Seleccione cliente:");
        cmbEstado.setSelectedItem("Activo");
        idCliente = 0;
    }

    private void cargarComboCliente() {
        String sql = "select * from tb_cliente";

        try (Connection cn = Conexion.conectar(); Statement st = cn.createStatement(); ResultSet rs = st.executeQuery(sql)) {

            cmbCliente.removeAllItems();
            cmbCliente.addItem("Seleccione cliente:");
            while (rs.next()) {
                cmbCliente.addItem(rs.getString("nombre") + " " + rs.getString("apellido"));
            }

        } catch (SQLException e) {
            System.out.println("Error al cargar cliente: " + e);
        }
    }

    private void obtenerIdCliente(String cliente) {
        String sql = "SELECT idCliente FROM tb_cliente WHERE CONCAT(nombre, ' ', apellido) = ?";

        try (Connection cn = Conexion.conectar(); PreparedStatement pst = cn.prepareStatement(sql)) {

            pst.setString(1, cliente);
            try (ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    idCliente = rs.getInt("idCliente");
                }
            }

        } catch (SQLException e) {
            System.out.println("Error al cargar el id cliente: " + e);
        }
    }

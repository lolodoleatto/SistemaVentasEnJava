    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Venta venta = new Venta();
        CtrlVenta ctrlVenta = new CtrlVenta();
        String cliente, estado;
        cliente = cmbCliente.getSelectedItem().toString().trim();
        estado = cmbEstado.getSelectedItem().toString().trim();

        //obtenemos id cliente
        this.obtenerIdCliente(cliente);

        //Actualizamos los datos
        if (!cliente.equalsIgnoreCase("Seleccione cliente:")) {
            venta.setIdCliente(idCliente);

            if (estado.equalsIgnoreCase("Activo")) {
                venta.setEstado(1);
            } else {
                venta.setEstado(0);
            }

            if (ctrlVenta.actualizar(venta, idVenta)) {
                JOptionPane.showMessageDialog(null, "Venta actualizada");
                this.cargarTablaVentas();
                this.cargarComboCliente();
                this.limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar venta");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un cliente para actualizar datos");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed



    //METODO PARA ALMACENAR EN LOS ARRAYLIST LOS DATOS A GRAFICAR
    private void almacenarDatos() {
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select fechaVenta, count(fechaVenta) as Ventas from tb_venta "
                    + "where fechaVenta BETWEEN '" + JInternalFrameGraficas.fechaInicio + "' "
                    + "and '" + JInternalFrameGraficas.fechaFin + "' "
                    + "group by fechaVenta");
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                listaFechas.add(rs.getString("fechaVenta"));
                listaCantidad.add(rs.getInt("Ventas"));
            }

        } catch (SQLException e) {
            System.out.println("Error en: " + e);
        }
    }

    //METODO PARA DETERMINAR CUAL ES EL DIA CON MAS VENTAS
    public int mayorVenta(ArrayList<Integer> listaCant) {
        int mayor = 0;
        for (int i = 0; i < listaCant.size(); i++) {
            if (listaCant.get(i) > mayor) {
                mayor = listaCant.get(i);
            }
        }

        return mayor;
    }

    //METODO PARA GRAFICAR LAS VENTAS
    @Override
    public void paint(Graphics g) {
        super.paint(g);

        int maxVenta = mayorVenta(listaCantidad);
        int largoNuevoIng = 0;
        int parametro1 = 133;
        int parametroFecha = 118;
        int parametro3 = 100;

        for (int i = 0; i < listaCantidad.size(); i++) {
            largoNuevoIng = listaCantidad.get(i) * 400 / maxVenta;

            switch (i) {
                case 0:
                    g.setColor(new Color(140, 0, 75)); //COLOR MORADO
                    break;
                case 1:
                    g.setColor(new Color(255, 0, 0)); //COLOR ROJO
                    break;
                case 2:
                    g.setColor(new Color(0, 0, 0)); //COLOR NEGRO
                    break;
                case 3:
                    g.setColor(new Color(255, 255, 255)); //COLOR BLANCO
                    break;
                case 4:
                    g.setColor(new Color(0, 85, 0)); //COLOR VERDE
                    break;
                default:
                    g.setColor(new Color(255, 127, 0)); //COLOR NARANJA
                    break;
            }

            g.fillRect(100, parametro3, largoNuevoIng, 40);
            g.drawString(listaFechas.get(i), 10, parametroFecha);
            g.drawString("Cantidad: " + listaCantidad.get(i), 10, parametro1);

            parametro1 += 50;
            parametroFecha += 50;
            parametro3 += 50;
        }
    }

// METODO PARA MOSTRAR TODOS LOS USUARIOS REGISTRADOS
    private void cargarTablaUsuarios() {
        DefaultTableModel model = new DefaultTableModel();
        String sql = "SELECT * from tb_usuario";

        try (Connection con = Conexion.conectar(); Statement st = con.createStatement(); ResultSet rs = st.executeQuery(sql)) {

            model.addColumn("Nro");
            model.addColumn("Nombre");
            model.addColumn("Apellido");
            model.addColumn("Usuario");
            model.addColumn("Contraseña");
            model.addColumn("Telefono");
            model.addColumn("Estado");

            while (rs.next()) {
                Object fila[] = new Object[7];

                fila[0] = rs.getInt("idUsuario");
                fila[1] = rs.getString("nombre");
                fila[2] = rs.getString("apellido");
                fila[3] = rs.getString("usuario");
                fila[4] = rs.getString("password");
                fila[5] = rs.getString("telefono");
                fila[6] = rs.getInt("estado");

                model.addRow(fila);
            }

            JInternalFrameGestionarUsuario.jTableUsuario = new JTable(model);
            JInternalFrameGestionarUsuario.jScrollCategoria.setViewportView(JInternalFrameGestionarUsuario.jTableUsuario);

        } catch (SQLException e) {
            System.out.println("Error al cargar la tabla: " + e.getMessage());
        }

        jTableUsuario.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int filaPoint = JInternalFrameGestionarUsuario.jTableUsuario.rowAtPoint(e.getPoint());
                int columnaPoint = 0;

                if (filaPoint > -1) {
                    idUsuario = Integer.parseInt(model.getValueAt(filaPoint, columnaPoint).toString());
                    EnviarDatosUsuario(idUsuario);
                }
            }
        });
    }

    private void EnviarDatosUsuario(int idUsuario) {
        String sql = "select * from tb_usuario where idUsuario = ?";
        try (Connection con = Conexion.conectar(); PreparedStatement pst = con.prepareStatement(sql)) {

            pst.setInt(1, idUsuario);

            try (ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    txtNombre.setText(rs.getString("nombre"));
                    txtApellido.setText(rs.getString("apellido"));
                    txtUsuario.setText(rs.getString("usuario"));
                    txtPassword.setText(rs.getString("password"));
                    txtTelefono.setText(rs.getString("telefono"));
                }
            }

        } catch (SQLException e) {
            System.out.println("Error al seleccionar el usuario: " + e.getMessage());
        }
    }

    private void limpiar() {
        txtNombre.setText("");
        txtApellido.setText("");
        txtUsuario.setText("");
        txtPassword.setText("");
        txtTelefono.setText("");
    }


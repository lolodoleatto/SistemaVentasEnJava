    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Usuario user = new Usuario();
        CtrlUsuario ctrlUser = new CtrlUsuario();

        if (idUsuario == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un usuario");
        } else {
            if (!txtNombre.getText().isEmpty() && !txtApellido.getText().isEmpty() && !txtUsuario.getText().isEmpty() && !txtPassword.getText().isEmpty()
                    && !txtTelefono.getText().isEmpty()) {
                //en caso de que los campos NO esten vacios

                user.setNombre(txtNombre.getText().trim());
                user.setApellido(txtApellido.getText().trim());
                user.setUsuario(txtUsuario.getText().trim());
                user.setContraseña(txtPassword.getText().trim());
                user.setTelefono(txtTelefono.getText().trim());
                user.setEstado(1);

                if (ctrlUser.actualizar(user, idUsuario)) {
                    //si el cliente se guarda
                    JOptionPane.showMessageDialog(null, "USUARIO ACTUALIZADO CORRECTAMENTE");
                    this.cargarTablaUsuarios();
                    this.limpiar();
                } else {
                    //si hay error en el guardado
                    JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR");
                    this.limpiar();
                }

            } else {
                JOptionPane.showMessageDialog(null, "COMPLETE TODOS LOS CAMPOS");
                this.limpiar();
            }
        }

        this.limpiar();
    }//GEN-LAST:event_btnActualizarActionPerformed


    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        CtrlUsuario ctrlUser = new CtrlUsuario();
        if (idUsuario == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un usuario");
        } else {
            if (ctrlUser.eliminar(idUsuario)) {
                JOptionPane.showMessageDialog(null, "Usuario eliminado");
                this.cargarTablaUsuarios();
                this.limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "ERROR AL ELIMINAR USUARIO");
                this.limpiar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

